namespace DormitoryApp
{
    public class HumanStudent
    {
        public string Name { get; set; }
        public string DormitoryNumber { get; set; }
        public string Room { get; set; }
        public string Treaty { get; set; }
        public string Contractual_effect { get; set; }
        public string Faculty { get; set; }
        public string Remarks { get; set; }
        public string Declaration { get; set; }

        public HumanStudent()
        {
            Name = string.Empty;
            DormitoryNumber = string.Empty;
            Room = string.Empty;
            Treaty = string.Empty;
            Contractual_effect = string.Empty;
            Faculty = string.Empty;
            Remarks = string.Empty;
            Declaration = string.Empty;
        }
    }
}
